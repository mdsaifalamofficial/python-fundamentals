# Lists in Python

numbers = [10, 20, 30, 40]
names = ["Saif", "Rahul", "Aman"]

print(numbers)
print(names)

# Indexing
print(names[0])
print(names[1])
print(names[2])

# Update list item
names[1] = "Rohit"
print(names)

# Append item
numbers.append(50)
print(numbers)

# Length of list
print(len(numbers))

# Loop through list
for name in names:
    print(name)
