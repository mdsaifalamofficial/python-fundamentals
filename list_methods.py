# List methods in Python

numbers = [1, 2, 3]

# append
numbers.append(4)
print(numbers)

# insert
numbers.insert(1, 10)
print(numbers)

# remove
numbers.remove(2)
print(numbers)

# pop
numbers.pop(1)
print(numbers)

# count
numbers.append(3)
print(numbers.count(3))

# sort
numbers.sort()
print(numbers)

# reverse
numbers.reverse()
print(numbers)

# clear
numbers.clear()
print(numbers)
